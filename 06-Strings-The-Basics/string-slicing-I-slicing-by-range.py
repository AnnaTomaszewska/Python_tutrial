adress = "Najlepsze miasto, Przasnysz, Krótka 10, 06300"
#slicing - wycinanie elementów
print(adress[0:5]) #pokazuje ile liter (0-pierwsza) do 5 (nie wliczając piątej)
print(adress[10:15])

print(adress[-5:-3]) #od końca
print(adress[5:]) #od 5 do końca
print(adress[:13]) #od początku do końca
print(adress[:]) #od początku do końca